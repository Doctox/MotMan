def main() -> None:
    document = json.loads(BLACKLIST.read_text(encoding="utf-8"))
    rejected = document.setdefault("rejectedAnswers", [])
    existing = set(rejected)
    additions = sorted((EDITORIAL_HARD_REJECTS | MANUAL_HARD_REJECTS) - existing)
    rejected.extend(additions)
    BLACKLIST.write_text(
        json.dumps(document, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(json.dumps({
        "added": len(additions),
        "totalRejectedAnswers": len(rejected),
        "answers": additions,
    }, ensure_ascii=False))


if __name__ == "__main__":
    main()
